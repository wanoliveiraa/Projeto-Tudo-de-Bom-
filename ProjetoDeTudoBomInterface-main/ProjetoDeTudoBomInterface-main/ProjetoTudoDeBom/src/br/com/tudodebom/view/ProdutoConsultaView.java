    private void readTabelaProduto(){
        DefaultTableModel tabela= (DefaultTableModel) tabelaProduto.getModel();
        tabelaProduto.setRowSorter(new TableRowSorter(tabela));
        tabela.setNumRows(0);
        for (Produto p : produtosDAO.getProdutos()) {
            tabela.addRow(new Object[]{
                p.getId_produto(),
                p.getNome_produto(),
                p.getQtd_produto(),
                p.isTipo_generico(),
                p.isTipo_medicamento(),
                p.getPreco()
            });

        }    
    }
     private void readTabelaProdutoNome(String nome){
        DefaultTableModel tabela= (DefaultTableModel) tabelaProduto.getModel();
        tabelaProduto.setRowSorter(new TableRowSorter(tabela));
        tabela.setNumRows(0);
        for (Produto p : produtosDAO.buscarPeloNomeProdutos(nome)) {
                tabela.addRow(new Object[]{
                p.getId_produto(),
                p.getNome_produto(),
                p.getQtd_produto(),
                p.isTipo_generico(),
                p.isTipo_medicamento(),
                p.getPreco()
            });
        }
                
    }
